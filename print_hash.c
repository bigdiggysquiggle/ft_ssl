#include "ssl_md5.h"

void	print_hash(char *s, int len)
{
	int	bytes;

	while (len)
	{
		bytes = *s++;
		bytes <<= 8;
		bytes |= *s++;
		ft_printf("%x", bytes);
		len -=2;
	}
}
